#4) 코드 설명

'''solution 함수에서 survey, choices의 값을 입력 받는다.
어떤 유형인지 저장하기 위해 answer을 초기 값으로 빈 문자열로 저장한다.
각 유형의 최종 점수를 구하기 위해 types이라는 딕셔너리를 만들고 모든 유형의 초기 값은 0으로 하여 생성한다.
반복문을 통해 i와 j에 각각 survey 값과 choices 값을 하나씩 집어넣는다.
반복문 안에서 j의 값이 4보다 작다면, 즉 매우 비동의, 비동의, 약간 비동의 중 하나라면 해당하는 점수를 딕셔너리에서 해당하는 유형의 점수에 추가한다.
만약 j가 4보다 크다면, 매우 동의, 동의, 약간 동의 중 하나라면 해당하는 점수를 딕셔너리에서 해당하는 유형의 점수에 추가한다.
반복문이 끝나고 나면 다른 반복문을 통해 유형들을 서로 비교하기 위해 key1과 key2값에다가 pairs라는 리스트에 요소 값을 집어넣는다.
반복문 안에서는 해당 key값들의 values를 가져와 value1과 value2에 저장한다.
만약 value1과 value2 값이 같다면 두 유형이 똑같이 나왔다는 뜻이므로 min 함수를 사용하여 최소값을 가져와 사전순으로 빠른 값을 answer에 저장한다.
만약 value1의 값이 더 크다면 key1을 answer에 저장하고 value2의 값이 더 크다면 key2를 answer에 저장한다.
반복문이 종료되면 answer 값을 반환한다.'''

