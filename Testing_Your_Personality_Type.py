def solution(survey, choices):
    answer = ''
    types = {"R":0, "T":0, "C":0, "F":0, "J":0, "M":0, "A":0, "N":0}
    pairs = [("R", "T"), ("C", "F"), ("J", "M"), ("A", "N")]
    for i,j in zip(survey,choices):
        if j < 4:
            types[i[0]] += 4-j
        if j > 4 :
            types[i[1]] += j-4
    for key1, key2 in pairs:
        value1 = types.get(key1)
        value2 = types.get(key2)
        if value1 == value2:
            answer += min(key1,key2)
        elif value1 > value2:
            answer += key1
        elif value1 < value2:
            answer += key2
    return answer



