def solution(progresses, speeds):
    answer = []
    while progresses:
        # 하루가 지나고 진도 업데이트
        progresses = [p + s for p, s in zip(progresses, speeds)]

        # 배포 가능한 기능 수 확인
        count = 0
        while progresses and progresses[0] >= 100:
            count += 1
            progresses.pop(0)
            speeds.pop(0)

        if count > 0:
            answer.append(count)

    return answer


